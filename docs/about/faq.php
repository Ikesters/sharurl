<h2>Frequently Asked Questions</h2>

<ol>
	<li>
		<h3>What is SharURL?</h3>
        <p>SharURL is a quick and easy way to share your files with friends and
        family. Simply select the files you want to share from your computer and
        a short, unique URL will be created to download those files.</p>
	</li>
	<li>
		<h3>How does it work?</h3>
        <p>SharURL leverages the technology in Adobe Flash to allow you to
        select multiple files at once and queues each file for upload. Once all
        the files have been uploaded to the server, they are zipped up and a
        unique URL is generated to download the zip file.</p>
	</li>
	<li>
		<h3>How do I select multiple files?</h3>
        <p>To select multiple files in the file dialog, start by selecting a
        single file by clicking on it. Now, hold down the Shift key and click on
        another file further down the list.</p>
	</li>
	<li>
		<h3>How much am I allowed to upload?</h3>
        <p>Accounts are currently allocated 2GB of space and 100GB of monthly
		data transfer.</p>
	</li>
	<li>
		<h3>Is there a maximum total upload limit?</h3>
        <p>Yes. Currently, the files you select can total no more than 2GB in
        size. If the size of the files you select total more than 2GB, you will
        be presented with a notice message.</p>
	</li>
	<li>
		<h3>How many files can I select?</h3>
        <p>There's currently no restriction on the number of files you can
        select. However, the total size of all your files can be no larger than
        2GB.</p>
	</li>
	<li>
		<h3>Is there a limit on the size of individual files?</h3>
		<p>Yes. Individual files are limited to 500MB in size. During testing
		we've found that attempts to upload files larger than 500MB resulted in
		web browser crashing.</p>
	</li>
	<li>
		<h3>Is there a limit on the number of times my file can be downloaded?</h3>
        <p>Sort of. Accounts are alloted 100GB of monthly transfers. If your
		account exceeds this, a notice will be shown at your download link.</p>
	</li>
	<li>
		<h3>Will my files ever expire?</h3>
        <p>No. They will remain available for download until you delete them.</p>
	</li>
	<li>
		<h3>Can I pay to increase the total upload limit or expiry date?</h3>
        <p>Not yet. Currently, we do not offer this service as we are still in
        the beta phase. However, value added services like increased upload
        limit, manual expiry date, SSL, and custom domain names are in the
        works.</p>
    </li>
</ol>
