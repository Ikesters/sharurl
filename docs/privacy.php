<h4>Personal Data Transmissions Are Encrypted</h4>
<p>
This Web site takes every precaution to protect our users' information.  When users submit sensitive information via the Web site, their information is protected both online and off-line.
When you order online from SharURL, all your personal information is protected. We use Secure Sockets Layer (SSL), the industry standard, for encrypting all personal information, including name, address and credit card numbers during the ordering process. Our staff also accesses your information with the same level of encryption to ensure that your information is transmitted securely between our staff's computers and the servers.
</p>

<h4>Other Secure Procedures</h4>
<p>
While we use SSL encryption to protect sensitive information online, we also do everything in our power to protect user-information off-line.  All of our users' information, not just the sensitive information mentioned above, is restricted in our offices.  Only employees who need the information to perform a specific job (for example, our billing clerk or a customer service representative) are granted access to personally identifiable information.  The servers that store personally identifiable information are in a secure environment, behind a locked cage, in a locked facility.
</p>

